#Change case in a String
name = "rodrigo ochoa"
print(name.title())

#Combining or Concatenating Strings
first_name = "rodrigo"
last_name = "ochoa"
full_name = first_name + " " + last_name
print(full_name.title())

print("Hello, " + full_name.title() + "!")

#Hacerlo como un mensaje
message = "Hello, " + full_name.title() + "!"
print(message)